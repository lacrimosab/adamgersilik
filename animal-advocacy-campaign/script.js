function applyLanguage(lang) {
  const dict = translations[lang] || translations.kz
  document.documentElement.lang = lang
  document.title = dict.page_title

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n")
    if (key && dict[key]) {
      el.textContent = dict[key]
    }
  })

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang)
  })

  localStorage.setItem("campaign-lang", lang)
}

const storedLang = localStorage.getItem("campaign-lang")
const defaultLang = storedLang || "kz"
applyLanguage(defaultLang)

document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => {
    const lang = btn.getAttribute("data-lang")
    if (lang) applyLanguage(lang)
  })
})

const revealItems = document.querySelectorAll(".reveal")
const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible")
        observer.unobserve(entry.target)
      }
    })
  },
  { threshold: 0.15 }
)

revealItems.forEach((item) => observer.observe(item))

document.querySelectorAll('.read-more-btn').forEach(button => {
  button.addEventListener('click', () => {
    const wrapper = button.parentElement.querySelector('.expandable-wrapper');
    const isExpanded = wrapper.classList.toggle('expanded');
    const newKey = isExpanded ? 'button_less' : 'button_more';
    button.setAttribute('data-i18n', newKey);

    const currentLang = document.documentElement.lang;
    if (typeof applyLanguage === "function") {
      applyLanguage(currentLang);
    }
  });
});